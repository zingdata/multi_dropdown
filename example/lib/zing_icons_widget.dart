//image icon based on name
  static dynamic getIcon(String name, {Color? color, double? size}) {
    // check if name is svg or png
    if (name.contains('.svg')) {
      return SvgPicture.asset(
        name,
        colorFilter: color != null ? ColorFilter.mode(color, BlendMode.srcIn) : null,
        width: size ?? 24,
        height: size ?? 24,
      );
    }

    return ImageIcon(
      AssetImage(name),
      color: color,
      size: size ?? 24,
    );
  }
